
public class GeometricObject {

	private String TColor;
	private boolean isfilled;
	
	public String getcolor() {
		return TColor;
	}
	public void putcolor(String c) { 
		this.TColor=c;
	}
	public void setFill (boolean fill) { 
		isfilled=fill;
	}
	public boolean isFilled() { 
		return isfilled;   
	}
}




